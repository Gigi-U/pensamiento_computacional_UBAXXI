# Operadores matemáticos en Python con ejemplos sencillos

# Suma (+)
a = 5
b = 3
resultado_suma = a + b
print("Suma:", resultado_suma)  # Output: 8

# Resta (-)
a = 5
b = 3
resultado_resta = a - b
print("Resta:", resultado_resta)  # Output: 2

# Multiplicación (*)
a = 5
b = 3
resultado_multiplicacion = a * b
print("Multiplicación:", resultado_multiplicacion)  # Output: 15

# División (/)
a = 6
b = 2
resultado_division = a / b
print("División:", resultado_division)  # Output: 3.0

# División entera (//)
a = 7
b = 2
resultado_division_entera = a // b
print("División entera:", resultado_division_entera)  # Output: 3

# Módulo (%) o resto
a = 7
b = 2
resultado_modulo = a % b
print("Módulo:", resultado_modulo)  # Output: 1

# Exponenciación (**)
a = 2
b = 3
resultado_exponenciacion = a ** b
print("Exponenciación:", resultado_exponenciacion)  # Output: 8

# Operador de asignación compuesta (+=)
a = 5
a += 3  # Equivalente a: a = a + 3
print("Operador de asignación compuesta:", a)  # Output: 8

# Operador de negación (-)
a = 5
resultado_negacion = -a
print("Operador de negación:", resultado_negacion)  # Output: -5


