# Operador de igualdad (==)
a = 5
b = 5
resultado_igualdad = a == b
print("Operador de igualdad:", resultado_igualdad)  # Output: True

# Operador de desigualdad (!=)
a = 5
b = 3
resultado_desigualdad = a != b
print("Operador de desigualdad:", resultado_desigualdad)  # Output: True

# Operadores de comparaci√≥n (<, <=, >, >=)
a = 5
b = 3
resultado_menor_que = a < b
print("Operador menor que:", resultado_menor_que)  # Output: False
resultado_menor_o_igual_que = a <= b
print("Operador menor o igual que:", resultado_menor_o_igual_que)  # Output: False
resultado_mayor_que = a > b
print("Operador mayor que:", resultado_mayor_que)  # Output: True
resultado_mayor_o_igual_que = a >= b
print("Operador mayor o igual que:", resultado_mayor_o_igual_que)  # Output: True

# Operadores booleanos (and, or, not)
x = True
y = False
resultado_and = x and y
print("Operador booleano AND:", resultado_and)  # Output: False
resultado_or = x or y
print("Operador booleano OR:", resultado_or)  # Output: True
resultado_not = not x
print("Operador booleano NOT:", resultado_not)  # Output: False


